import os
import joblib
import numpy as np
import pandas as pd
import streamlit as st
import plotly.graph_objects as go

# Set page configurations
st.set_page_config(
    page_title="Wi-Fi Intruder Detector",
    page_icon="🔒",
    layout="wide",
    initial_sidebar_state="expanded"
)

# Feature columns used by the classifier (must match training order)
FEATURE_COLUMNS = [
    'packet_count',
    'avg_packet_size',
    'max_packet_size',
    'min_packet_size',
    'tcp_count',
    'udp_count',
    'total_bytes',
    'tcp_udp_ratio',
    'avg_inter_arrival_time'
]

# Load model and label encoder with st.cache_resource
@st.cache_resource
def load_ml_assets():
    model_path = os.path.join('model', 'device_classifier.pkl')
    encoder_path = os.path.join('model', 'label_encoder.pkl')
    
    if not os.path.exists(model_path) or not os.path.exists(encoder_path):
        return None, None
        
    try:
        model = joblib.load(model_path)
        label_encoder = joblib.load(encoder_path)
        return model, label_encoder
    except Exception as e:
        st.sidebar.error(f"Error loading model files: {e}")
        return None, None

# Load assets
model, label_encoder = load_ml_assets()

# Sidebar Layout
st.sidebar.title("🛠️ Settings & Config")
st.sidebar.markdown("---")

# Data Source Selection
data_source = st.sidebar.radio(
    "Data Source",
    ["Upload CSV", "Use Default Dataset (dataset/wifi_dataset.csv)"],
    index=1 if os.path.exists(os.path.join('dataset', 'wifi_dataset.csv')) else 0
)


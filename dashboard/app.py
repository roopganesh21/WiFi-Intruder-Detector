# Helper predict function accepting a dataframe row
def predict_device_row(row: pd.Series, model, label_encoder, threshold: float) -> dict:
    # Convert series to DataFrame with exact column order
    df_row = pd.DataFrame([row])[FEATURE_COLUMNS]
    
    # Run prediction probabilities
    probabilities = model.predict_proba(df_row)[0]
    
    # Get max confidence and predicted class
    max_idx = np.argmax(probabilities)
    confidence = float(probabilities[max_idx])
    decoded_label = label_encoder.inverse_transform([max_idx])[0]
    
    is_unknown = confidence < threshold
    predicted_label = "Unknown Device Detected" if is_unknown else decoded_label
    
    return {
        "predicted_label": predicted_label,
        "confidence": confidence,
        "is_unknown": is_unknown,
        "original_label": decoded_label
    }

